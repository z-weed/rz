# @return variance, given a standard deviation.
# @begin what_is_sample_and_population_variance_of_sdev
what_is_sample_and_population_variance_of_sdev <- function(a) { a ^ 2 }
what_is_sample_and_population_variance_of_sdev(100)
# @end what_is_sample_and_population_variance_of_sdev

# @return StandardDeviation of variance
# @begin what_is_sdev_of_variance
what_is_sdev_of_variance <- function(a) { sqrt(a) }
what_is_sdev_of_variance(16)
# @end what_is_sdev_of_variance


